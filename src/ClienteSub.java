    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        try {
            MqttClient cliente_sub = new MqttClient("tcp://mqtt.eclipseprojects.io:1883", "cliente_sub");
            cliente_sub.connect();
            System.out.println("Cliente Conectado");
            cliente_sub.subscribe("/temp", 0);
            cliente_sub.subscribe("/sala/luz", 0);

            cliente_sub.setCallback(new MqttCallback() {
                @Override
                public void connectionLost(Throwable thrwbl) {
                    System.out.println("Conex√£o perdida");
                }

                @Override
                public void messageArrived(String topic, MqttMessage msg) throws Exception {
                    if (topic.equals("/temp")) {

                        String temperatura = msg.toString();
                        if (temperatura.equals("40")) {
                            valorTemperatura.setText(temperatura + " ta quente");

                        } else {
                            valorTemperatura.setText(temperatura);

                        }
                    } else {
                        JOptionPane.showMessageDialog(null, msg.toString());
                        String msgP = "Oi publicador";
                        MqttMessage mqttMsg = new MqttMessage(msgP.getBytes());
                        cliente_sub.publish("/resposta", mqttMsg);
                    }
                }

                @Override
                public void deliveryComplete(IMqttDeliveryToken imdt) {
                    System.out.println("Entrega feita com sucesso");
                }
            });

        } catch (MqttException ex) {
            System.out.println("Nao foi possivel se conectar");
            Logger.getLogger(ClienteSub.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1MouseClicked

